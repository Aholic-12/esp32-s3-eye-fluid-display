void lcd_deinit(lcd_handle_t lcd)
{
    lcd_t *handle = (lcd_t *)lcd;
    if (handle == NULL) {
        return;
    }

    lcd_backlight_set(false);

    for (int i = 0; i < 2; i++) {
        if (handle->framebuffers[i] != NULL) {
            heap_caps_free(handle->framebuffers[i]);
            handle->framebuffers[i] = NULL;
        }
    }
    handle->draw_buffer = NULL;
    if (handle->panel != NULL) {
        esp_lcd_panel_del(handle->panel);
        handle->panel = NULL;
    }
    if (handle->panel_io != NULL) {
        esp_lcd_panel_io_del(handle->panel_io);
        handle->panel_io = NULL;
    }
    spi_bus_free(LCD_SPI_HOST);
}
