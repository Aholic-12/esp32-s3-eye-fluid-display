static void lcd_backlight_init(void)
{
    const gpio_config_t bk_cfg = {
        .pin_bit_mask = 1ULL << LCD_PIN_NUM_BK_LIGHT,
        .mode = GPIO_MODE_OUTPUT,
        .pull_up_en = GPIO_PULLUP_DISABLE,
        .pull_down_en = GPIO_PULLDOWN_DISABLE,
        .intr_type = GPIO_INTR_DISABLE,
    };
    gpio_config(&bk_cfg);
    gpio_set_level(LCD_PIN_NUM_BK_LIGHT, 0);
}

static void lcd_backlight_set(bool on)
{
    gpio_set_level(LCD_PIN_NUM_BK_LIGHT, on ? 1 : 0);
}

static void lcd_draw_char(lcd_handle_t lcd, int x, int y, char ch, uint16_t color, int scale)
{
    lcd_t *handle = (lcd_t *)lcd;
    if (handle == NULL || ch < 32 || ch > 126) {
        return;
    }

    if (scale < 1) {
        scale = 1;
    }

    const uint8_t *font = font_8x16[ch - 32];
    for (int row = 0; row < 16; row++) {
        for (int col = 0; col < 8; col++) {
            if ((font[row] & (1U << (7 - col))) == 0) {
                continue;
            }
            const int px = x + col * scale;
            const int py = y + row * scale;
            for (int dy = 0; dy < scale; dy++) {
                for (int dx = 0; dx < scale; dx++) {
                    lcd_draw_pixel(lcd, px + dx, py + dy, color);
                }
            }
        }
    }
}

lcd_handle_t lcd_init(void)
{
    esp_err_t ret;
    memset(&s_lcd, 0, sizeof(s_lcd));

    lcd_backlight_init();

    const spi_bus_config_t bus_cfg = {
        .mosi_io_num = LCD_PIN_NUM_DATA,
        .miso_io_num = -1,
        .sclk_io_num = LCD_PIN_NUM_CLK,
        .quadwp_io_num = -1,
        .quadhd_io_num = -1,
        .max_transfer_sz = LCD_MAX_TRANSFER_SZ,
    };

    ret = spi_bus_initialize(LCD_SPI_HOST, &bus_cfg, SPI_DMA_CH_AUTO);
    if (ret != ESP_OK && ret != ESP_ERR_INVALID_STATE) {
        ESP_LOGE(TAG, "spi bus init failed: %s", esp_err_to_name(ret));
        return NULL;
    }

    const esp_lcd_panel_io_spi_config_t io_config = {
        .dc_gpio_num = LCD_PIN_NUM_DC,
        .cs_gpio_num = LCD_PIN_NUM_CS,
        .pclk_hz = LCD_SPI_FREQ_HZ,
        .lcd_cmd_bits = 8,
        .lcd_param_bits = 8,
        .spi_mode = 0,
        .trans_queue_depth = 10,
    };

    ret = esp_lcd_new_panel_io_spi(LCD_SPI_HOST, &io_config, &s_lcd.panel_io);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "panel io init failed: %s", esp_err_to_name(ret));
        return NULL;
    }

    const esp_lcd_panel_dev_config_t panel_config = {
        .reset_gpio_num = LCD_PIN_NUM_RST,
        .rgb_ele_order = LCD_RGB_ELEMENT_ORDER_RGB,
        .bits_per_pixel = 16,
    };

    ret = esp_lcd_new_panel_st7789(s_lcd.panel_io, &panel_config, &s_lcd.panel);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "panel init failed: %s", esp_err_to_name(ret));
        return NULL;
    }

    ESP_ERROR_CHECK(esp_lcd_panel_reset(s_lcd.panel));
    ESP_ERROR_CHECK(esp_lcd_panel_init(s_lcd.panel));
    ESP_ERROR_CHECK(esp_lcd_panel_invert_color(s_lcd.panel, true));
    ESP_ERROR_CHECK(esp_lcd_panel_swap_xy(s_lcd.panel, false));
    ESP_ERROR_CHECK(esp_lcd_panel_mirror(s_lcd.panel, false, false));
    ESP_ERROR_CHECK(esp_lcd_panel_disp_on_off(s_lcd.panel, true));

    for (int i = 0; i < 2; i++) {
        s_lcd.framebuffers[i] = (uint16_t *)heap_caps_malloc(LCD_WIDTH * LCD_HEIGHT * sizeof(uint16_t), MALLOC_CAP_DMA | MALLOC_CAP_8BIT);
        if (s_lcd.framebuffers[i] == NULL) {
            ESP_LOGE(TAG, "framebuffer[%d] alloc failed", i);
            return NULL;
        }
        memset(s_lcd.framebuffers[i], 0, LCD_WIDTH * LCD_HEIGHT * sizeof(uint16_t));
    }
    s_lcd.draw_index = 0;
    s_lcd.draw_buffer = s_lcd.framebuffers[s_lcd.draw_index];

    lcd_backlight_set(true);
    ESP_LOGI(TAG, "lcd init ok (%dx%d)", LCD_WIDTH, LCD_HEIGHT);
    return &s_lcd;
}

void lcd_clear(lcd_handle_t lcd, uint16_t color)
{
    lcd_t *handle = (lcd_t *)lcd;
    if (handle == NULL || handle->draw_buffer == NULL) {
        return;
    }

    for (int i = 0; i < LCD_WIDTH * LCD_HEIGHT; i++) {
        handle->draw_buffer[i] = color;
    }
}

